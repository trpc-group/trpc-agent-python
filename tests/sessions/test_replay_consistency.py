async def _cleanup_sql_replay_data(session_service: Any, memory_service: Any) -> None:
    """Best-effort cleanup for env SQL backends; scoped to this test run's app prefix."""
    app_prefix = f"replay-{RUN_ID}-%"
    save_key_prefix = f"replay-{RUN_ID}-%"
    try:
        async with session_service._sql_storage.create_db_session() as sql_session:
            for storage_cls in (SessionStorageEvent, StorageSession, StorageAppState):
                await session_service._sql_storage.delete(
                    sql_session,
                    SqlKey(key=tuple(), storage_cls=storage_cls),
                    SqlCondition(filters=[storage_cls.app_name.like(app_prefix)]),
                )
            await session_service._sql_storage.delete(
                sql_session,
                SqlKey(key=tuple(), storage_cls=StorageUserState),
                SqlCondition(filters=[StorageUserState.app_name.like(app_prefix)]),
            )
            await session_service._sql_storage.commit(sql_session)

        async with memory_service._sql_storage.create_db_session() as sql_session:
            await memory_service._sql_storage.delete(
                sql_session,
                SqlKey(key=tuple(), storage_cls=MemStorageEvent),
                SqlCondition(filters=[MemStorageEvent.save_key.like(save_key_prefix)]),
            )
            await memory_service._sql_storage.commit(sql_session)
    except Exception:
        # Cleanup must not hide the replay failure that triggered teardown.
        return


async def _cleanup_redis_replay_data(session_service: Any, memory_service: Any) -> None:
    """Best-effort cleanup for env Redis backends; scoped to this test run's app prefix."""
    key_patterns = [
        f"session:replay-{RUN_ID}-*",
        f"app_state:replay-{RUN_ID}-*",
        f"user_state:replay-{RUN_ID}-*",
        f"memory:replay-{RUN_ID}-*",
    ]
    try:
        async with session_service._redis_storage.create_db_session() as redis_session:
            for pattern in key_patterns[:3]:
                keys = await session_service._redis_storage.execute_command(
                    redis_session,
                    RedisCommand(method="keys", args=(pattern, )),
                )
                for key in keys:
                    await session_service._redis_storage.delete(redis_session, key)

        async with memory_service._redis_storage.create_db_session() as redis_session:
            keys = await memory_service._redis_storage.execute_command(
                redis_session,
                RedisCommand(method="keys", args=(key_patterns[3], )),
            )
            for key in keys:
                await memory_service._redis_storage.delete(redis_session, key)
    except Exception:
        # Cleanup must not hide the replay failure that triggered teardown.
        return


def _backend_builders() -> list[ReplayBackendBuilder]:
    available_builders: dict[str, ReplayBackendBuilder] = {
        "in_memory": ReplayBackendBuilder("in_memory", _build_in_memory_backend),
        "sqlite": ReplayBackendBuilder("sqlite", _build_sqlite_backend),
    }
    if os.environ.get(SQL_URL_ENV):
        available_builders["sql_env"] = ReplayBackendBuilder("sql_env", _build_sql_env_backend)
    if os.environ.get(REDIS_URL_ENV):
        available_builders["redis_env"] = ReplayBackendBuilder("redis_env", _build_redis_env_backend)

    selected_backends = [name.strip() for name in os.environ.get(BACKENDS_ENV, "").split(",") if name.strip()]
    if selected_backends:
        missing = [name for name in selected_backends if name not in available_builders]
        if missing:
            raise ValueError(f"Unknown or unavailable replay backend(s): {missing}. "
                             f"Available backends: {sorted(available_builders)}")
        return [available_builders[name] for name in selected_backends]

    builders = [available_builders["in_memory"], available_builders["sqlite"]]
    if "sql_env" in available_builders:
        builders.append(available_builders["sql_env"])
    if "redis_env" in available_builders:
        builders.append(available_builders["redis_env"])
    return builders


def _part_from_config(part: dict[str, Any]) -> Part:
    kind = part["kind"]
    if kind == "text":
        return Part.from_text(text=part["text"])
    if kind == "function_call":
        return Part(function_call=FunctionCall(name=part["name"], args=part["args"]))
    if kind == "function_response":
        return Part(function_response=FunctionResponse(name=part["name"], response=part["response"]))
    raise ValueError(f"Unsupported part kind: {kind}")


def _parts_from_operation(operation: dict[str, Any]) -> list[Part]:
    if "parts" in operation:
        return [_part_from_config(part) for part in operation["parts"]]
    if "text" in operation:
        return [Part.from_text(text=operation["text"])]
    if operation["op"] == "append_tool_call":
        return [Part(function_call=FunctionCall(name=operation["name"], args=operation["args"]))]
    if operation["op"] == "append_tool_response":
        return [Part(function_response=FunctionResponse(name=operation["name"], response=operation["response"]))]
    raise ValueError(f"Unsupported event content operation: {operation['op']}")


def _event_metadata(operation: dict[str, Any]) -> dict[str, Any]:
    metadata = {}
    for key in (
            "branch",
            "request_id",
            "parent_invocation_id",
            "tag",
            "filter_key",
            "requires_completion",
            "version",
            "visible",
            "model_flags",
    ):
        if key in operation:
            metadata[key] = operation[key]
    if "long_running_tool_ids" in operation:
        metadata["long_running_tool_ids"] = set(operation["long_running_tool_ids"])
    return metadata


def _make_event(operation: dict[str, Any]) -> Event:
    return Event(
        id=operation["event_id"],
        invocation_id=operation["invocation_id"],
        author=operation["author"],
        content=Content(parts=_parts_from_operation(operation)),
        actions=EventActions(state_delta=operation.get("state_delta", {})),
        partial=operation.get("partial", False),
        timestamp=_operation_timestamp(operation),
        **_event_metadata(operation),
    )


def _event_from_operation(operation: dict[str, Any]) -> Event:
    if operation["op"] in {"append_text", "append_parts", "append_tool_call", "append_tool_response", "failed_append"}:
        return _make_event(operation)
    raise ValueError(f"Unsupported event operation: {operation['op']}")


def _operation_timestamp(operation: dict[str, Any]) -> float:
    return time.time() + REPLAY_EVENT_FUTURE_OFFSET_SECONDS + float(operation["timestamp"]) / 1000.0


async def _drive_case(case: ReplayCase, backend: ReplayBackend) -> ReplayRunResult:
    sessions: dict[str, Session] = {}
    last_session_ref: Optional[str] = None

    def _session_ref(operation: dict[str, Any]) -> str:
        return operation.get("session_ref", "default")

    def _require_session(operation: dict[str, Any]) -> Session:
        ref = _session_ref(operation)
        if ref not in sessions:
            raise AssertionError(f"Case {case.name} references unknown session {ref}")
        return sessions[ref]

    for operation in case.operations:
        op = operation["op"]
        if op == "create_session":
            ref = _session_ref(operation)
            sessions[ref] = await backend.session_service.create_session(
                app_name=operation.get("app_name", case.app_name),
                user_id=operation.get("user_id", case.user_id),
                session_id=operation["session_id"],
                state=operation.get("state"),
            )
            last_session_ref = ref
        elif op in {"append_text", "append_parts", "append_tool_call", "append_tool_response"}:
            session = _require_session(operation)
            await backend.session_service.append_event(session, _event_from_operation(operation))
        elif op == "failed_append":
            session = _require_session(operation)
            _simulate_failed_append_before_persist(backend, session, _event_from_operation(operation))
        elif op == "store_memory":
            session = _require_session(operation)
            await backend.memory_service.store_session(session)
        elif op == "summarize":
            session = _require_session(operation)
            await backend.session_service.create_session_summary(session)
            reloaded = await backend.session_service.get_session(
                app_name=session.app_name,
                user_id=session.user_id,
                session_id=session.id,
            )
            if reloaded is None:
                raise AssertionError(f"Case {case.name} lost session {session.id} after summarization")
            sessions[_session_ref(operation)] = reloaded
        else:
            raise ValueError(f"Unsupported replay operation: {op}")

    if not sessions:
        raise AssertionError(f"Case {case.name} did not create a session")

    stored_sessions = {}
    for ref, session in sessions.items():
        stored_session = await backend.session_service.get_session(
            app_name=session.app_name,
            user_id=session.user_id,
            session_id=session.id,
        )
        if stored_session is None:
            raise AssertionError(f"Case {case.name} lost session {session.id} on backend {backend.name}")
        stored_sessions[ref] = stored_session

    primary_ref = case.primary_session_ref
    if primary_ref not in stored_sessions:
        primary_ref = last_session_ref or next(iter(stored_sessions))
    return ReplayRunResult(primary_session=stored_sessions[primary_ref], sessions=stored_sessions)


def _simulate_failed_append_before_persist(backend: ReplayBackend, session: Session, event: Event) -> None:
    """Mutate the caller-side session like append_event does, then fail before backend persistence."""
    try:
        if hasattr(backend.session_service, "_append_event_to_session"):
            # Test-only fault injection: model the window where the caller-side
            # Session was mutated, but the backend write failed before commit.
            backend.session_service._append_event_to_session(session, event)
        raise RuntimeError("simulated replay append failure before backend persistence")
    except RuntimeError:
        return


def _normalize_summary_text(text: str | None) -> str | None:
    if text is None:
        return None
    return " ".join(text.split()).strip().lower()


def _stable(value: Any) -> Any:
    if isinstance(value, dict):
        return {key: _stable(value[key]) for key in sorted(value)}
    if isinstance(value, list):
        return [_stable(item) for item in value]
    return value


def _normalize_parts(content: Content | None) -> list[dict[str, Any]]:
    if content is None or not content.parts:
        return []
    parts = []
    for part in content.parts:
        if part.text is not None:
            parts.append({"kind": "text", "text": part.text})
        elif part.function_call is not None:
            parts.append({
                "kind": "function_call",
                "name": part.function_call.name,
                "args": _stable(part.function_call.args or {}),
            })
        elif part.function_response is not None:
            parts.append({
                "kind": "function_response",
                "name": part.function_response.name,
                "response": _stable(part.function_response.response or {}),
            })
    return parts


def _normalize_event(event: Event, index: int) -> dict[str, Any]:
    state_delta = event.actions.state_delta if event.actions else {}
    return {
        "index": index,
        "id": "<generated-summary-id>" if event.is_summary_event() else event.id,
        "timestamp": "<normalized>",
        "author": event.author,
        "invocation_id": event.invocation_id,
        "parts": _normalize_parts(event.content),
        "state_delta": _stable(state_delta),
        "is_summary": event.is_summary_event(),
        "model_visible": event.is_model_visible(),
        "partial": bool(event.partial),
        "branch": event.branch,
        "request_id": event.request_id,
        "parent_invocation_id": event.parent_invocation_id,
        "tag": event.tag,
        "filter_key": event.filter_key,
        "requires_completion": event.requires_completion,
        "version": event.version,
        "visible": event.visible,
        "model_flags": event.model_flags,
        "long_running_tool_ids": sorted(event.long_running_tool_ids or []),
    }


def _normalize_memory_response(response: Any) -> list[dict[str, Any]]:
    memories = []
    for memory in response.memories:
        memories.append({
            "author": memory.author,
            "timestamp": "<normalized>",
            "parts": _normalize_parts(memory.content),
        })
    return sorted(memories, key=lambda item: json.dumps(item, sort_keys=True, ensure_ascii=False))


def _summary_payload_text(event: Event | None) -> str | None:
    if event is None:
        return None
    text = event.get_text()
    prefix = "Previous conversation summary:"
    if text.startswith(prefix):
        text = text[len(prefix):]
    return _normalize_summary_text(text)


def _has_storage_timestamp(event: Event) -> bool:
    return isinstance(event.timestamp, (int, float)) and event.timestamp > 0


def _summary_records(session: Session) -> list[dict[str, Any]]:
    records = []
    ordered_summary_events = [
        ("historical", event) for event in session.historical_events if event.is_summary_event()
    ] + [("active", event) for event in session.events if event.is_summary_event()]
    for version, (source, event) in enumerate(ordered_summary_events, start=1):
        records.append({
            "summary_id": f"{session.id}:summary:v{version}",
            "session_id": session.id,
            "version": version,
            "source": source,
            "active": source == "active",
            "updated_at": "<normalized>",
            "updated_at_present": _has_storage_timestamp(event),
            "text": _summary_payload_text(event),
        })
    return records


def _normalize_summary_from_session(session: Session) -> dict[str, Any]:
    active_summary_events = [event for event in session.events if event.is_summary_event()]
    historical_summary_events = [event for event in session.historical_events if event.is_summary_event()]
    latest_summary_event = active_summary_events[-1] if active_summary_events else None
    version_records = _summary_records(session)
    version = len(version_records)
    latest_record = version_records[-1] if version_records else None

    return {
        "exists": latest_summary_event is not None,
        "id": latest_record["summary_id"] if latest_summary_event and latest_record else None,
        "session_id": session.id if latest_summary_event else None,
        "version": version,
        "text": _summary_payload_text(latest_summary_event),
        "has_summary_anchor": latest_summary_event is not None,
        "anchor_text": _normalize_summary_text(latest_summary_event.get_text() if latest_summary_event else None),
        "active_summary_count": len(active_summary_events),
        "historical_summary_count": len(historical_summary_events),
        "versions": version_records,
        "latest_version_active": latest_record["active"] if latest_record else None,
        "latest_version_source": latest_record["source"] if latest_record else None,
        "updated_at_present": _has_storage_timestamp(latest_summary_event) if latest_summary_event else False,
        "historical_event_count": len(session.historical_events),
        "retained_event_count": len(session.events),
        "updated_at": "<normalized>" if latest_summary_event else None,
    }


def _empty_service_summary() -> dict[str, Any]:
    return {
        "service_public_exists": False,
        "service_public_text": None,
        "service_manager_exists": False,
        "service_manager_text": None,
        "service_manager_session_id": None,
        "service_manager_original_event_count": None,
        "service_manager_compressed_event_count": None,
        "service_manager_updated_at": None,
    }


async def _normalize_summary_from_service(
    backend: ReplayBackend,
    session: Session,
    *,
    include_manager_metadata: bool = True,
) -> dict[str, Any]:
    public_summary_text = await backend.session_service.get_session_summary(session)
    summary = _empty_service_summary()
    summary["service_public_exists"] = public_summary_text is not None
    summary["service_public_text"] = _normalize_summary_text(public_summary_text)

    if not include_manager_metadata:
        return summary

    manager = getattr(backend.session_service, "summarizer_manager", None)
    if manager is None:
        return summary

    summary_obj = await manager.get_session_summary(session)
    if summary_obj is None:
        return summary

    summary.update({
        "service_manager_exists": True,
        "service_manager_text": _normalize_summary_text(summary_obj.summary_text),
        "service_manager_session_id": summary_obj.session_id,
        "service_manager_original_event_count": summary_obj.original_event_count,
        "service_manager_compressed_event_count": summary_obj.compressed_event_count,
        "service_manager_updated_at": "<normalized>",
    })
    return summary


async def _snapshot_backend(
    case: ReplayCase,
    backend: ReplayBackend,
    result: ReplayRunResult,
    *,
    include_service_summary: bool = True,
    include_cached_summary_metadata: bool = True,
) -> dict[str, Any]:
    session = result.primary_session

    memory_results = {}
    for query_config in case.memory_queries:
        query = query_config["query"]
        query_session_ref = query_config.get("session_ref", case.primary_session_ref)
        query_session = result.sessions[query_session_ref]
        query_name = query_config.get("name", query)
        response = await backend.memory_service.search_memory(
            query_session.save_key,
            query,
            limit=query_config.get("limit", 10),
        )
        memory_results[query_name] = _normalize_memory_response(response)

    summary = _normalize_summary_from_session(session)
    if include_service_summary:
        summary.update(
            await _normalize_summary_from_service(
                backend,
                session,
                include_manager_metadata=include_cached_summary_metadata,
            ))
    else:
        summary.update(_empty_service_summary())

    return {
        "backend": backend.name,
        "case": case.name,
        "session": {
            "id": session.id,
            "app_name": session.app_name,
            "user_id": session.user_id,
            "state": _stable(session.state),
            "last_update_time": "<normalized>",
        },
        "events": [_normalize_event(event, index) for index, event in enumerate(session.events)],
        "historical_events": [_normalize_event(event, index) for index, event in enumerate(session.historical_events)],
        "sessions": {
            ref: _snapshot_session(stored_session)
            for ref, stored_session in sorted(result.sessions.items())
        },
        "memory": memory_results,
        "summary": summary,
    }


def _snapshot_session(session: Session) -> dict[str, Any]:
    return {
        "id": session.id,
        "app_name": session.app_name,
        "user_id": session.user_id,
        "state": _stable(session.state),
        "last_update_time": "<normalized>",
        "events": [_normalize_event(event, index) for index, event in enumerate(session.events)],
        "historical_events": [_normalize_event(event, index) for index, event in enumerate(session.historical_events)],
        "summary": _normalize_summary_from_session(session),
    }


async def _run_case_on_backend(
    case: ReplayCase,
    backend_builder: ReplayBackendBuilder | Callable[[], Awaitable[ReplayBackend]],
) -> dict[str, Any]:
    build = backend_builder.build if isinstance(backend_builder, ReplayBackendBuilder) else backend_builder
    backend = await build()
    try:
        result = await _drive_case(case, backend)
        return await _snapshot_backend(case, backend, result)
    finally:
        await backend.close()


