async def _run_all_cases() -> tuple[list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]], list[str], float]:
    start = time.monotonic()
    cases = _load_replay_cases()
    builders = _backend_builders()
    attempted_backends = [builder.name for builder in builders]
    case_reports = []
    all_diffs = []
    all_errors = []
    for case in cases:
        snapshots = []
        errors = []
        for builder in builders:
            try:
                snapshots.append(await _run_case_on_backend(case, builder))
            except Exception as exc:  # pylint: disable=broad-except
                error = _backend_error_report(case, builder.name, exc)
                errors.append(error)
                all_errors.append(error)

        if not snapshots:
            case_reports.append({
                "case": case.name,
                "description": case.description,
                "backends": [],
                "diffs": [],
                "errors": errors,
                "snapshots": [],
            })
            continue

        reference = snapshots[0]
        diffs = []
        for candidate in snapshots[1:]:
            diffs.extend(_compare_snapshots(reference, candidate))

        all_diffs.extend(diffs)
        case_reports.append({
            "case": case.name,
            "description": case.description,
            "backends": [snapshot["backend"] for snapshot in snapshots],
            "diffs": diffs,
            "errors": errors,
            "snapshots": snapshots,
        })
    return case_reports, all_diffs, all_errors, attempted_backends, time.monotonic() - start


