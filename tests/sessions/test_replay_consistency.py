@dataclass(frozen=True)
class ReplayCase:
    name: str
    description: str
    operations: list[dict[str, Any]]
    memory_queries: list[dict[str, Any]]
    primary_session_ref: str = "default"

    @property
    def app_name(self) -> str:
        return f"replay-{RUN_ID}-{self.name}"

    @property
    def user_id(self) -> str:
        return "replay-user"


@dataclass
class ReplayBackend:
    name: str
    session_service: Any
    memory_service: Any
    db_url: str | None = None
    cleanup_paths: list[Path] = field(default_factory=list)
    cleanup_callback: Callable[[], Awaitable[None]] | None = None

    async def close(self, cleanup_files: bool = True) -> None:
        try:
            if self.cleanup_callback:
                await self.cleanup_callback()
        finally:
            await self.memory_service.close()
            await self.session_service.close()
        if cleanup_files:
            for path in self.cleanup_paths:
                for candidate in (path, Path(f"{path}-wal"), Path(f"{path}-shm")):
                    try:
                        candidate.unlink()
                    except FileNotFoundError:
                        pass


