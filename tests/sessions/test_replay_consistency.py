async def _reload_sessions_from_backend(
    backend: ReplayBackend,
    sessions: dict[str, Session],
    primary_session_ref: str,
) -> ReplayRunResult:
    reloaded_sessions = {}
    for ref, session in sessions.items():
        reloaded = await backend.session_service.get_session(
            app_name=session.app_name,
            user_id=session.user_id,
            session_id=session.id,
        )
        if reloaded is None:
            raise AssertionError(f"Reloaded backend lost session {session.id}")
        reloaded_sessions[ref] = reloaded

    primary_ref = primary_session_ref if primary_session_ref in reloaded_sessions else next(iter(reloaded_sessions))
    return ReplayRunResult(primary_session=reloaded_sessions[primary_ref], sessions=reloaded_sessions)


def _event_index_from_path(path: str) -> int | None:
    match = re.search(r"\.events\[(\d+)\]", path)
    if not match:
        match = re.search(r"\.historical_events\[(\d+)\]", path)
    return int(match.group(1)) if match else None


def _make_diff(
    *,
    case_name: str,
    session_id: str,
    backend: str,
    reference_backend: str,
    path: str,
    reference: Any,
    actual: Any,
) -> dict[str, Any]:
    return {
        "case": case_name,
        "session_id": session_id,
        "event_index": _event_index_from_path(path),
        "summary_id": _summary_id_from_path(session_id, path),
        "backend": backend,
        "reference_backend": reference_backend,
        "path": path,
        "reference": reference,
        "actual": actual,
    }


def _summary_id_from_path(session_id: str, path: str) -> str | None:
    if not re.match(r"^\$(?:\.summary|\.sessions\.[^.[]+\.summary)", path):
        return None
    match = re.match(r"^\$(?:\.summary|\.sessions\.[^.[]+\.summary)\.versions\[(\d+)\]", path)
    if match:
        return f"{session_id}:summary:v{int(match.group(1)) + 1}"
    return f"{session_id}:summary"


def _allowed_diff_reason(path: str) -> str | None:
    for rule in ALLOWED_DIFFS:
        pattern = re.escape(rule["path"])
        pattern = pattern.replace(r"\[\*\]", r"\[\d+\]")
        pattern = pattern.replace(r"\*", r"[^.\[\]]+")
        if re.fullmatch(pattern, path):
            return rule["reason"]
    return None


def _session_id_for_path(reference_snapshot: dict[str, Any], path: str) -> str:
    match = re.match(r"^\$\.sessions\.([^.[]+)", path)
    if match:
        session_ref = match.group(1)
        nested_session = reference_snapshot.get("sessions", {}).get(session_ref, {})
        if nested_session.get("id"):
            return nested_session["id"]
    return reference_snapshot["session"]["id"]


def _compare_values(
    reference: Any,
    actual: Any,
    *,
    path: str,
    case_name: str,
    session_id_for_path: Callable[[str], str],
    backend: str,
    reference_backend: str,
) -> list[dict[str, Any]]:
    if _allowed_diff_reason(path):
        return []

    if type(reference) is not type(actual):
        return [
            _make_diff(
                case_name=case_name,
                session_id=session_id_for_path(path),
                backend=backend,
                reference_backend=reference_backend,
                path=path,
                reference=reference,
                actual=actual,
            )
        ]

    diffs: list[dict[str, Any]] = []
    if isinstance(reference, dict):
        for key in sorted(set(reference) | set(actual)):
            next_path = f"{path}.{key}"
            if key not in reference or key not in actual:
                diffs.append(
                    _make_diff(
                        case_name=case_name,
                        session_id=session_id_for_path(next_path),
                        backend=backend,
                        reference_backend=reference_backend,
                        path=next_path,
                        reference=reference.get(key, "<missing>"),
                        actual=actual.get(key, "<missing>"),
                    ))
            else:
                diffs.extend(
                    _compare_values(
                        reference[key],
                        actual[key],
                        path=next_path,
                        case_name=case_name,
                        session_id_for_path=session_id_for_path,
                        backend=backend,
                        reference_backend=reference_backend,
                    ))
        return diffs

    if isinstance(reference, list):
        if len(reference) != len(actual):
            diffs.append(
                _make_diff(
                    case_name=case_name,
                    session_id=session_id_for_path(f"{path}.length"),
                    backend=backend,
                    reference_backend=reference_backend,
                    path=f"{path}.length",
                    reference=len(reference),
                    actual=len(actual),
                ))
        for index, (left, right) in enumerate(zip(reference, actual)):
            diffs.extend(
                _compare_values(
                    left,
                    right,
                    path=f"{path}[{index}]",
                    case_name=case_name,
                    session_id_for_path=session_id_for_path,
                    backend=backend,
                    reference_backend=reference_backend,
                ))
        return diffs

    if reference != actual:
        diffs.append(
            _make_diff(
                case_name=case_name,
                session_id=session_id_for_path(path),
                backend=backend,
                reference_backend=reference_backend,
                path=path,
                reference=reference,
                actual=actual,
            ))
    return diffs


def _compare_snapshots(reference: dict[str, Any], actual: dict[str, Any]) -> list[dict[str, Any]]:
    def session_id_for_path(path: str) -> str:
        return _session_id_for_path(reference, path)

    return _compare_values(
        {
            key: value
            for key, value in reference.items() if key != "backend"
        },
        {
            key: value
            for key, value in actual.items() if key != "backend"
        },
        path="$",
        case_name=reference["case"],
        session_id_for_path=session_id_for_path,
        backend=actual["backend"],
        reference_backend=reference["backend"],
    )


def _write_report(path: Path, report: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(report, indent=2, sort_keys=True, ensure_ascii=False), encoding="utf-8")


def _resolve_report_path(tmp_path: Path) -> Path:
    configured_path = os.environ.get(REPORT_PATH_ENV)
    if not configured_path:
        return tmp_path / REPORT_FILE_NAME

    report_path = Path(configured_path)
    if report_path.suffix:
        return report_path
    return report_path / REPORT_FILE_NAME


